export type TranslationTree = typeof translations.en;
export type TranslationKey =
  | `common.${keyof TranslationTree['common'] & string}`
  | `drawer.${keyof TranslationTree['drawer'] & string}`
  | `login.${keyof TranslationTree['login'] & string}`
  | `home.${keyof TranslationTree['home'] & string}`
  | `remote.${keyof TranslationTree['remote'] & string}`
  | `computer.${keyof TranslationTree['computer'] & string}`
  | `addDevices.${keyof TranslationTree['addDevices'] & string}`
  | `settings.${keyof TranslationTree['settings'] & string}`;

export function getTranslation(language: Language, key: TranslationKey): string {
  const [section, leaf] = key.split('.') as [keyof typeof translations.en, string];
  const value = translations[language][section][leaf as never];
  return typeof value === 'string' ? value : key;
}
